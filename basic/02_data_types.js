//Data Types ...
// * There are 7 datatypes available in javascript. They can be retrive by `typeof keyword`

// number -> 0,1.2 11.45,123212,...
// string -> `Sagar`,`123`,`Moni121%42e`,...
// boolean ->  true,false
// undefined -> undefined
// object -> objects,arrays,functions,null
// symbol(es6) -> ...

console.log(typeof 0); // number
console.log(typeof "Sagar"); // string
console.log(typeof null); //! object
console.log(typeof undefined); // undefined
console.log(typeof {}); // object
console.log(typeof true); // boolean
